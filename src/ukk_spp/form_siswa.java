    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if (txtNisn.getText().equals("") || txtNis.getText().equals("") || txtNama.getText().equals("") || cbKelas.getSelectedItem().equals("") || txtAlamat.getText().equals("") || txtNotelp.getText().equals("") || cbTahun.getSelectedItem().equals("") || cbJk.getSelectedItem().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Data harus terisi semua!");
        }else {
        try {
            PreparedStatement ps = con.prepareStatement("INSERT INTO siswa VALUES (?,?,?,?,?,?,?,?)");
            ps.setString(1, txtNisn.getText());
            ps.setString(2, txtNis.getText());
            ps.setString(3, txtNama.getText());
            ps.setString(4, txtIdkelas.getText());
            ps.setString(5, txtAlamat.getText());
            ps.setString(6, txtNotelp.getText());
            ps.setString(7, txtIdspp.getText());
            ps.setString(8, cbJk.getSelectedItem().toString());
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data telah tersimpan");
            refreshTable();
            txtNisn.setText("");
            txtNis.setText("");
            txtNama.setText("");
            cbKelas.setSelectedItem("");
            txtAlamat.setText("");
            txtNotelp.setText("");
            cbTahun.setSelectedItem("");
            cbJk.setSelectedItem("");
        } catch (Exception e) {
            System.out.print("EROR KUERI KE DATABASE:\n" + e + "\n\n");   
        }
        }
    }//GEN-LAST:event_btnTambahActionPerformed

