    private void refreshTable(){
        tm = new DefaultTableModel(null, new Object[] {"NISN", "NIS", "Nama Siswa", "Nama Kelas", "Alamat", "No Telepon", "Tahun", "Jenis Kelamin"});
        tbDatasiswa.setModel(tm);
        tm.getDataVector().removeAllElements();
        try{
            PreparedStatement s = con.prepareStatement("SELECT siswa.nisn, siswa.nis, siswa.nama, kelas.nama_kelas, siswa.alamat, siswa.no_telp, spp.tahun, siswa.jenis_kelamin FROM siswa INNER JOIN kelas USING(id_kelas) INNER JOIN spp USING(id_spp)");
            ResultSet r = s.executeQuery();
            while(r.next()){
                Object[] data ={
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6),
                    r.getString(7),
                    r.getString(8)
                };
                tm.addRow(data);
                }
            }catch(Exception e){
                System.out.print("ERROR QUERY KE DATABSE:\n" + e + "\n\n");
            
        }
    }
    
    public void ambilData(){
        try {
            PreparedStatement s = con.prepareStatement("SELECT * FROM spp");
            ResultSet r = s.executeQuery();
                while (r.next()) {
                    cbTahun.addItem(r.getString("tahun"));
                }
        }catch(Exception e){           
        }
    }
    
    public void Data(){
        try {
            PreparedStatement s = con.prepareStatement("SELECT * FROM kelas");
            ResultSet r = s.executeQuery();
                while (r.next()) {
                    cbKelas.addItem(r.getString("nama_kelas"));
                }
        }catch(Exception e){           
        }
    }
   

