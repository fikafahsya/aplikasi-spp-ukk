    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
        tm = new DefaultTableModel(null, new Object[] {"NISN", "NIS", "Nama Siswa", "Nama Kelas", "Alamat", "No Telepon", "Tahun", "Jenis Kelamin"});
        tbDatasiswa.setModel(tm);
        tm.getDataVector().removeAllElements();
        try {
            PreparedStatement s = con.prepareStatement("SELECT siswa.nisn, siswa.nis, siswa.nama, kelas.nama_kelas, siswa.alamat, siswa.no_telp, spp.tahun, siswa.jenis_kelamin FROM siswa INNER JOIN kelas USING(id_kelas) INNER JOIN spp USING(id_spp) WHERE nama LIKE '%" + txtCari.getText().toString() +"%'");
            ResultSet r = s.executeQuery();
            while(r.next()) {
                Object[] data ={
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5),
                    r.getString(6),
                    r.getString(7),
                    r.getString(8)            
                };
                tm.addRow(data);
            }
        } catch(Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_btnCariActionPerformed

    private void tbDatasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDatasiswaMouseClicked
        // TODO add your handling code here:
        int i = tbDatasiswa.getSelectedRow();
        if (i == -1) {
            //tidak ada baris terseleksi
            return;

        }

        String nisn = (String) tm.getValueAt(i, 0);
        txtNisn.setText(nisn);

        String nis = (String) tm.getValueAt(i, 1);
        txtNis.setText(nis);

        String namasiswa = (String) tm.getValueAt(i, 2);
        txtNama.setText(namasiswa);
        
        String kelas = (String) tm.getValueAt(i, 3);
        cbKelas.setSelectedItem(kelas);
        
        String alamat = (String) tm.getValueAt(i, 4);
        txtAlamat.setText(alamat);
        
        String notelepon = (String) tm.getValueAt(i, 5);
        txtNotelp.setText(notelepon);
        
        String idspp = (String) tm.getValueAt(i, 6);
        cbTahun.setSelectedItem(idspp);
        
        String jk = (String) tm.getValueAt(i, 7);
        cbJk.setSelectedItem(jk);
    }//GEN-LAST:event_tbDatasiswaMouseClicked

